# Python Primitive Data Types
#
# String

print("Hello"[4])  # string
print("12345"[4])  # still string
print("123" + "78")  # again still string

# Integer
print(123 + 78)  # integer
print(123_456_789)  # still integer and _ is ignored

# Float
print(3.14159)  # Float

# Boolean
print(True)  # or print(False) -> Boolean
